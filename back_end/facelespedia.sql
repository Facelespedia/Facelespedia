    TeamID int,
    TeamName varchar(255),
    TeamsubName varchar(255),
    Rating int,
    Nation varchar(255)
);

CREATE TABLE Hero(
    HeroID int,
    HeroName varchar(255),
    HeroType varchar(255),
    HeroAttackType varchar(255),
    HeroRole varchar(255)
);

